# Complexidade: O(N*M)
#
# - Onde N e M sÃ£o as dimensÃµes da matriz.


L = 10
C = 12

matriz = [
    ['#','.','.','#','#','#','.','#','#','.','#','#'],
    ['#','#','.','#','#','.','.','.','#','#','#','.'],
    ['.','#','#','.','.','#','.','.','.','.','.','#'],
    ['#','.','.','#','.','.','.','.','#','#','#','#'],
    ['.','#','.','#','.','#','.','.','#','.','.','#'],
    ['.','.','#','#','#','#','#','.','#','.','.','.'],
    ['.','.','#','.','.','#','.','.','#','.','.','#'],
    ['.','.','#','.','.','#','#','#','.','.','.','#'],
    ['.','#','.','#','.','.','.','#','#','.','#','.'],
    ['#','#','.','.','.','.','#','.','.','.','.','.']
]

adjL = (-1,0,1,0)
adjC = (0,1,0,-1)

# FunÃ§Ã£o Flood Fill
def floodFill(x, y):
    matriz[x][y] = 'V'

    for i in range(4):
        l = x + adjL[i]
        c = y + adjC[i]

        if l >= 0 and c >= 0 and l < L and c < C and matriz[l][c] == '.':
            floodFill(l, c)
        


floodFill(2, 6)

